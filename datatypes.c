//Write a C program to demonstrate the use of different data types (int, float, char,double). 
#include <stdio.h>
#include <conio.h>
void main()
{ 
    int i = 45;
    float f = 2.88;
    char c = 'B';
    double d = 28.888;

    printf("Integer variable: %d\n", i);
    printf("Float variable: %f\n", f);
    printf("Character variable: %c\n", c);
    printf("Double variable: %lf\n", d);

    getch();
}